        // Main은 프로그램의 첫 시작을 나타냄

        int num;  // int(int는 정수 타입)가 데이터형태 num이 데이터(상자) 이름으로 선언
        num = 100;  // num이라는 변수에 값 100을 대입(할당)했다
        System.out.println(num); // num 상자에 들어있는 데이터를 출력 if(""로 감쌀 시에는 문자로 생각)

        int num2 = 200; // 변수 선언과 동시에 값을 대입함
